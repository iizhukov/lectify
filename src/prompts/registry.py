def get_prompt(prompt_name: str) -> str:
    """
    Возвращает текст промпта. Сначала пытается прочитать файл из resources/prompts,
    если файла нет или имя не соответствует — берет встроенный дефолтный промпт.
    """
    subject_file = RESOURCES_PROMPTS_DIR / f"{prompt_name}.txt"
    if subject_file.exists():
        try:
            return subject_file.read_text(encoding="utf-8")
        except Exception:
            raise Exception(f"Failed to read prompt file: {subject_file}")
            
    return DEFAULT_PROMPTS.get(prompt_name, "")
